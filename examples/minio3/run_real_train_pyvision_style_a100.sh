LORA_RANK=${LORA_RANK:-8} \
LORA_ALPHA=${LORA_ALPHA:-16} \
ACTOR_LR=${ACTOR_LR:-1e-6} \
USE_KL_LOSS=${USE_KL_LOSS:-False} \
KL_LOSS_COEF=${KL_LOSS_COEF:-0.000} \
ENTROPY_COEFF=${ENTROPY_COEFF:-0.000} \
CLIP_RATIO_HIGH=${CLIP_RATIO_HIGH:-0.3} \
CLIP_RATIO_LOW=${CLIP_RATIO_LOW:-0.2} \
MAX_NUM_GEN_BATCHES=${MAX_NUM_GEN_BATCHES:-256} \
TOTAL_EPOCHS=${TOTAL_EPOCHS:-100} \
TOTAL_TRAINING_STEPS=${TOTAL_TRAINING_STEPS:-1} \
SAVE_FREQ=${SAVE_FREQ:-10} \
SAVE_LORA_ONLY=${SAVE_LORA_ONLY:-True} \
TEST_FREQ=${TEST_FREQ:-5} \
LOG_VAL_GENERATIONS=${LOG_VAL_GENERATIONS:-512} \
VAL_BEFORE_TRAIN=${VAL_BEFORE_TRAIN:-False} \
VAL_ONLY=${VAL_ONLY:-False} \
NGPUS_PER_NODE=${NGPUS_PER_NODE:-8} \
